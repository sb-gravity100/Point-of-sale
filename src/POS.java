    private void notifyPayment() {
        payNotif.setText("Not enough cash.");
        payNotif.setForeground(Color.red);
    }

    private void addQty (String num) {
        int amount = Integer.parseInt(num);
        int current = Integer.parseInt(qtyField.getText()) + amount;
        if (amount == 0) {
            current = 0;
        }
        qtyField.setText("" + current);
        Double price = Double.valueOf(priceField.getText());
        Double total = price * current;
        totalField.setText(String.format("%.2f", total));
    } 
    private void pickProduct(String id, String name) {
        String[] prod = id.split("-");
        String key = prod[0];
        Double price = products.get(key);
        productField.setText(name);
        priceField.setText(String.format("%.2f", price));
        qtyField.setText("1");
        totalField.setText(String.format("%.2f", price));
        checkButtons();
    }
    
    private void addData(Object [] data) {
        DefaultTableModel model = (DefaultTableModel) mainTable.getModel();
        Boolean flag = true;
        int index = -1;
        for (int i = 0; i < model.getRowCount(); i++) {
            String name =  model.getValueAt(i, 0).toString();
            if (data[0].equals(name)) {
                flag = false;
                index = i;
                break;
            }
        }
        if (flag) {
            model.addRow(data);
        } else {
            Double current = Double.valueOf(model.getValueAt(index, 3).toString());
            int qty = Integer.parseInt(model.getValueAt(index, 2).toString());
            Double total = Double.valueOf(data[3].toString()) + current;
            int totalQty = Integer.parseInt(data[2].toString()) + qty;
            model.setValueAt(String.valueOf(totalQty), index, 2);
            model.setValueAt(String.format("%.2f", total), index, 3);
        }
        updateTotal();
        if (!finalTotal.getText().equals("0.00")) {
            Pay.setEnabled(true);
        } else {
            Pay.setEnabled(false);
        }
    }
    
    private void clear() {
        productField.setText("");
        priceField.setText("0.00");
        qtyField.setText("0");
        totalField.setText("0.00");
        
        checkButtons();
    }
    
    private void updateTotal() {
        DefaultTableModel model = (DefaultTableModel) mainTable.getModel();
        Double total = 0.00;
        for (int i = 0; i < model.getRowCount(); i++) {
            total += Double.valueOf(model.getValueAt(i, 3).toString());
        }
        finalTotal.setText(String.format("%.2f", total));
    }
    
    private void clearTable() {
        ((DefaultTableModel)mainTable.getModel()).setRowCount(0);
    }
    
    private void clearCash() {
        payField.setText("0");
        payChange.setText("0.00");
    }
    
    private void resetAll() {
        receipt.setText(horizontalLine);
        finalTotal.setText("0.00");
        clear();
        clearTable();
        clearCash();
        checkButtons();
    }
    
    private void disableButtons() {
        oneQtyBtn.setEnabled(false);
        clearQty.setEnabled(false);
        hundredQtyBtn.setEnabled(false);
        fiftyQtyBtn.setEnabled(false);
        twentyQtyBtn.setEnabled(false);
        fiveQtyBtn.setEnabled(false);
        twoQtyBtn.setEnabled(false);
        addBtn.setEnabled(false);
    }
    
    private void checkButtons() {
        if (productField.getText().isEmpty()) {
            oneQtyBtn.setEnabled(false);
            clearQty.setEnabled(false);
            hundredQtyBtn.setEnabled(false);
            fiftyQtyBtn.setEnabled(false);
            twentyQtyBtn.setEnabled(false);
            fiveQtyBtn.setEnabled(false);
            twoQtyBtn.setEnabled(false);
        } else {
            oneQtyBtn.setEnabled(true);
            clearQty.setEnabled(true);
            hundredQtyBtn.setEnabled(true);
            fiftyQtyBtn.setEnabled(true);
            twentyQtyBtn.setEnabled(true);
            fiveQtyBtn.setEnabled(true);
            twoQtyBtn.setEnabled(true);
        }
        
        if (Double.parseDouble(totalField.getText()) == 0.00) {
            addBtn.setEnabled(false);
        } else {
            addBtn.setEnabled(true);
        }
        
        if (!finalTotal.getText().equals("0.00")) {
            Pay.setEnabled(true);
        } else {
            Pay.setEnabled(false);
        }
    }
    
    private void insertLine() {
        addText(horizontalLine + "\n");
    }
    private void nextLine() {
        addText("\n");
    }
    private void insertTab() {
        addText("\t");
    }
    
    private void insertTab(int num) {
        for (int i = 0; i < num; i++) {
            addText("\t");
        }
    }
    
    private void addText(String text) {
        receipt.setText(receipt.getText() + text);
    }
    
    private void clearText() {
        receipt.setText("");
    }
    
    private void receiptHeader() {
        Date date = new Date();
        String text  = "";
        text += "\n\t";
        text += title;
        text += "\n\t";
        text += String.format("%-21s %11s", "Date:", formatter.format(date));
        text += "\n\t";
        text += String.format("%-22s %11s", "Transaction ID:", String.valueOf(date.getTime()));
        addText(text);
        nextLine();
        nextLine();
        insertLine();
        insertTab();
        addText(String.format("%-13s %-8s %-5s   %-10s\n", "Name", "Price", "Qty", "Total"));
        nextLine();
    }
    
    private void printProducts() {
        DefaultTableModel model = (DefaultTableModel) mainTable.getModel();
        for (int i = 0; i < model.getRowCount(); i++) {
            insertTab();
            String name = model.getValueAt(i, 0).toString();
            int qty = Integer.parseInt(model.getValueAt(i, 2).toString());
            Double price = Double.valueOf(model.getValueAt(i, 1).toString());
            Double total = Double.valueOf(model.getValueAt(i, 3).toString());
            String text = String.format("%.15s %7.2f %5d %10.2f\n", name, price, qty, total);
            addText(text);
        }
    }
    
    private void printTotal() {
        nextLine();
        insertLine();
        String text  = "";
        text += "\n\t";
        text += String.format("%-25s %10.2f", "Total:", Double.valueOf(finalTotal.getText()));
        text += "\n\t";
        text += String.format("%-25s %10.2f", "Cash:", Double.valueOf(payField.getText()));
        text += "\n\t";
        text += String.format("%-25s %10.2f", "Cash:", Double.valueOf(payChange.getText()));
        text += "\n";
        addText(text);
    }
